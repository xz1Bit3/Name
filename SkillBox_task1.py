total_tasks = 0
answered_call = False

print("Начался восьмичасовой рабочий день.\n")

for hour in range(1, 9):
    print(f"{hour}-й час")
    tasks = int(input("Сколько задач решит Максим? "))
    total_tasks += tasks

    if not answered_call:
        answer = input("Звонит жена. Взять трубку? (1 — да, 0 — нет): ")
        if answer == '1':
            answered_call = True
    print()  # Пустая строка между часами

print(f"\nРабочий день закончился. Всего выполнено задач: {total_tasks}")
if answered_call:
    print("Нужно зайти в магазин.")

