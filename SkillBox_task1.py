# а другой пытается его угадать. Программа должна запрашивать число у пользователя до тех пор,
# пока тот не угадает загаданное. После каждой попытки выводите подсказки, больше или меньше загаданного введённое число.
# Также после отгадки выводите количество попыток.
# Выводите сообщения в соответствии с примером.
# Пример (загадали число 7)
# Введите число: 3
# Число меньше, чем нужно. Попробуйте ещё раз!
# Введите число: 10
# Число больше, чем нужно. Попробуйте ещё раз!
# Введите число: 8
# Число больше, чем нужно. Попробуйте ещё раз!
# Введите число: 7
# Вы угадали! Число попыток: 4
# Что оценивается
# Формат вывода соответствует заданию.


print("Игра 'Угадай число от 1 до 10'")
secret_number = int(input('Игрок №1, загадайте ваше число :'))
attempts = 0

while True:
    guess = int(input('Игрок №2, какое число было загадано? :'))
    attempts += 1

    if guess < secret_number:
        print('Число больше, чем нужно. Попробуйте ещё раз!')
    elif guess > secret_number:
        print('Число меньше, чем нужно. Попробуйте ещё раз!')
    else:
        print(f'Вы угадали! Число попыток: {attempts}')
        break
